///           в”Ӯ       в”Ӯ      в”Ӯ       в”Ӯ        в”Ӯ       в”Ӯ
///           в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ      в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ        в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
///
///             int8           int64              utf8view
/// ```
///
struct Test {
    /// The input parquet file
    parquet: Bytes,
    /// which selections to make -- each selection is a range of row indices to
    /// include The tests verify that only rows within these ranges are returned
    /// and select
    selections: Vec<Range<usize>>,
}

impl Test {
    /// Basic idea is to evaluate the selections in several different ways and compare the results
    fn run(&self) {
        for projection in self.all_projections() {
            println!("projection: {:?}", projection);
            let expected = self.expected(&projection);

            for batch_size in [100, 217, 8192] {
                println!("  batch size: {}", batch_size);

                self.run_inner(projection, batch_size, &expected, |b| {
                    self.add_predicate_row_selection(b)
                });

                self.run_inner(projection, batch_size, &expected, |b| {
                    self.add_predicate_row_filter_int8(b)
                });
            }
        }
    }

    /// Create a reader with the specified projection and batch size,
    /// and function F to apply the selection predicates.
    ///
    /// Reads all rows and verify the results match expected
    fn run_inner<F>(
        &self,
        projection: &[&str],
        batch_size: usize,
        expected: &RecordBatch,
        add_predicate: F,
    ) where
        F: Fn(ParquetRecordBatchReaderBuilder<Bytes>) -> ParquetRecordBatchReaderBuilder<Bytes>,
    {
        let builder = self
            .builder_with_projection(projection)
            .with_batch_size(batch_size);
        let reader = add_predicate(builder).build().unwrap();
        let actual = self.collect_to_batch(reader);

        assert_eq!(
            expected, &actual,
            "selections: {projection:?}, batch size: {batch_size}\n\
                            expected:\n\
                            \n\
                            {expected:#?}\n\
                            \n\
                            {actual:#?}",
        );
    }

    /// A list of columns to project
    fn all_projections(&self) -> &[&[&str]] {
        &[
            &["int8"],
            //["int64"],
            //["utf8view"], ["int8", "int64"], ["int64", "utf8view"], ["int8", "utf8view"],
            &["int8", "int64", "utf8view"],
        ]
    }

    /// Return a ParquetRecordBatchReaderBuilder ready to read the parquet file
    fn builder_with_projection(
        &self,
        projection: &[&str],
    ) -> ParquetRecordBatchReaderBuilder<Bytes> {
        let builder = ParquetRecordBatchReaderBuilder::try_new(self.parquet.clone()).unwrap();

        let projection = ProjectionMask::columns(
            builder.metadata().file_metadata().schema_descr(),
            projection.iter().cloned(),
        );
        builder.with_projection(projection)
    }

    /// Apply the selections to the three_column_batch to get the expected result
    fn expected(&self, columns: &[&str]) -> RecordBatch {
        let batch = three_column_batch();
        let indices = columns
            .iter()
            .map(|col_name| batch.schema().index_of(col_name).unwrap())
            .collect::<Vec<_>>();

        let batch = batch.project(&indices).unwrap();
        let batches: Vec<_> = self
            .selections
            .iter()
            .map(|range| {
                // collect expected rows from the three_column_batch
                batch.slice(range.start, range.end - range.start)
            })
            .collect();

        concat_batches(&batch.schema(), &batches).unwrap()
    }

    fn collect_to_batch(&self, mut reader: ParquetRecordBatchReader) -> RecordBatch {
        let mut batches = vec![];
        while let Some(batch) = reader.next() {
            let batch = batch.unwrap();
            batches.push(batch);
        }
        let schema = batches[0].schema();
        concat_batches(&schema, &batches).unwrap()
    }

    // ------------------
    // Below are different predicates are added to the reader. Note these
    // methods all select the same selections, just in different ways.
    // ------------------

    /// Add a predicate to the reader that selects only the specified rows
    fn add_predicate_row_selection<T: ChunkReader>(
        &self,
        builder: ParquetRecordBatchReaderBuilder<T>,
    ) -> ParquetRecordBatchReaderBuilder<T> {
        let num_rows = builder.metadata().file_metadata().num_rows() as usize;
        let selection =
            RowSelection::from_consecutive_ranges(self.selections.clone().into_iter(), num_rows);
        builder.with_row_selection(selection)
    }

    /// Add a predicate to the reader via a predicate on the "int64" column
    fn add_predicate_row_filter_int8<T: ChunkReader>(
        &self,
        builder: ParquetRecordBatchReaderBuilder<T>,
    ) -> ParquetRecordBatchReaderBuilder<T> {
        let mask = ProjectionMask::columns(
            builder.metadata().file_metadata().schema_descr(),
            vec!["int8"],
        );
        let filter = RowFilter::new(vec![Box::new(ArrowPredicateFn::new(mask, |array| todo!()))]);
        builder.with_row_filter(filter)
    }
}
